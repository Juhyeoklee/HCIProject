import UIKit

extension UIColor {
    
    @nonobjc class var greenblue: UIColor {
        return UIColor(red: 35.0 / 255.0, green: 204.0 / 255.0, blue: 135.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var veryLightPink: UIColor {
        return UIColor(white: 214.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var veryLightPinkTwo: UIColor {
        return UIColor(white: 185.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var brownGrey16: UIColor {
        return UIColor(white: 181.0 / 255.0, alpha: 1)
    }
    
    @nonobjc class var brownGrey: UIColor {
      return UIColor(white: 162.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var veryLightPinkThree: UIColor {
      return UIColor(white: 213.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var brownGreyTwo: UIColor {
      return UIColor(white: 160.0 / 255.0, alpha: 1.0)
    }

}
